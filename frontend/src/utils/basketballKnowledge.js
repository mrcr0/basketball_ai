var dateTimePatterns = [
  { pattern: /今天.*几号|今天.*日期|今天.*号|今天.*日/, type: 'date' },
  { pattern: /现在.*几点|现在.*时间|几点了|什么时间/, type: 'time' },
  { pattern: /今天.*星期|今天.*周几/, type: 'weekday' }
]

export function localResponse(question) {
  if (!question) return null

  var q = question.toLowerCase().trim()

  for (var i = 0; i < dateTimePatterns.length; i++) {
    var dt = dateTimePatterns[i]
    if (dt.pattern.test(q)) {
      var now = new Date()
      if (dt.type === 'date') {
        return '📅 今天是 ' + now.getFullYear() + '年' + (now.getMonth() + 1) + '月' + now.getDate() + '日'
      }
      if (dt.type === 'time') {
        return '🕐 现在是 ' + String(now.getHours()).padStart(2, '0') + ':' + String(now.getMinutes()).padStart(2, '0') + ':' + String(now.getSeconds()).padStart(2, '0')
      }
      if (dt.type === 'weekday') {
        var days = ['日', '一', '二', '三', '四', '五', '六']
        return '📅 今天是星期' + days[now.getDay()]
      }
    }
  }

  if (/你好|嗨|哈喽|hello|hi/i.test(q)) {
    return '👋 你好！我是篮球AI助手，可以为你解答篮球训练、战术、规则、体能等方面的问题。试试问我："如何提高投篮命中率？"或"什么是挡拆战术？"'
  }

  if (/谢谢|感谢|thanks|thank/i.test(q)) {
    return '不客气！加油训练，祝你球技越来越好！🏀💪'
  }

  var bestMatch = null
  var bestScore = 0

  for (var k = 0; k < knowledgeBase.length; k++) {
    var entry = knowledgeBase[k]
    var score = 0
    for (var j = 0; j < entry.keywords.length; j++) {
      if (q.includes(entry.keywords[j])) {
        score += entry.keywords[j].length
      }
    }
    if (score > bestScore) {
      bestScore = score
      bestMatch = entry
    }
  }

  if (bestScore >= 2) {
    return bestMatch.answer
  }

  return null
}

